from collections import defaultdict

report = defaultdict(dict)

for empID, surname, name, fatherName in employees:
    report[empID]["FIO"] = f"{surname} {name} {fatherName}"
    report[empID]["salary"] = 0
    report[empID]["count_salary"] = 0
    report[empID]["bonus"] = 0
    report[empID]["count_bonus"] = 0
    report[empID]["Emails"] = set()
    
for id, empID, email in emails:
    report[empID]["Emails"].add(email)
    
for empID, date, salaryType, amount in salary:
    report[empID][salaryType] += amount
    report[empID][f'count_{salaryType}'] += 1
    
for key, dict_value in report.items():
    id = key
    FIO = dict_value['FIO']
    avg_salary = 0 if dict_value['count_salary'] == 0 else dict_value['salary'] / dict_value['count_salary']
    avg_bonus = 0 if dict_value['count_bonus'] == 0 else dict_value['bonus'] / dict_value['count_bonus']
    if dict_value['Emails']:
        for email in dict_value['Emails']:
            print(f"{id}, {FIO}, {avg_salary}, {avg_bonus}, {email}")
    else:
        print(f"{id}, {FIO}, {avg_salary}, {avg_bonus}, {None}")
    